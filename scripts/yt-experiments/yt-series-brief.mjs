#!/usr/bin/env node
/**
 * yt-series-brief — deep-research → show-bible writer. Runs the ohwow
 * deep-research flow against a series-specific prompt, writes the result
 * to docs/youtube/<slug>-showbible.md, and (via yt-series-bootstrap)
 * uploads it to the workspace knowledge base so agents can RAG into it.
 *
 * Usage:
 *   node --import tsx scripts/yt-experiments/yt-series-brief.mjs briefing
 *
 * This script is a SKELETON for Phase 1 — it wraps the
 * ohwow_deep_research MCP tool when called from a Claude Code session
 * OR falls back to a normal llm({ purpose: 'reasoning' }) call otherwise.
 * The show-bible skeletons already in docs/youtube/ give enough shape
 * to start without this; upgrade this script once the first unlisted
 * upload is clean.
 */
import fs from 'node:fs';
import path from 'node:path';
import { llm } from '../x-experiments/_ohwow.mjs';
import { getSeries } from '../../src/integrations/youtube/series/registry.js';

const slug = process.argv[2];
if (!slug) {
  console.error('usage: yt-series-brief.mjs <slug>');
  process.exit(2);
}
const series = getSeries(slug);

const researchPrompt = `Research question: what makes a great recurring ${series.displayName} YouTube Short in 2026?

Context:
- Role: ${series.role}
- Tagline: "${series.tagline}"
- Audience: OHWOW.FUN viewers — founders, operators, AI-curious professionals, tech enthusiasts.

Produce a show bible with:
1. A sharp one-liner defining the show.
2. The 3-second identity — what makes every episode unmistakably this series.
3. Voice (tone, pace, register).
4. Format contract — scene breakdown with time ranges, narration word-count bands.
5. Source rules — what seeds make great material, what to avoid.
6. Banned phrases + patterns — ship-killers.
7. Success metric + threshold.
8. 10 hook templates that have worked in the wild (cite real creators where known).
9. 10 failure modes and how to catch them before posting.
10. Curriculum of 30 episode ideas grounded in current 2026 culture.

Output a comprehensive Markdown document. 1500+ words. Cite real creators and real patterns where you can.`;

async function main() {
  console.log(`[series-brief] researching ${series.displayName}...`);
  const out = await llm({
    purpose: 'reasoning',
    system: 'You are a senior YouTube-native creator-strategist. You have watched thousands of Shorts and know why certain formats land. You answer in publication-ready Markdown.',
    prompt: researchPrompt,
  });
  const biblePath = path.resolve(`docs/youtube/${slug}-showbible.md`);
  const header = `# Show Bible — ${series.displayName}\n\n> **Status:** generated by yt-series-brief.mjs on ${new Date().toISOString().slice(0, 10)}.\n> Review before publishing. Edits here flow to the prompt module + knowledge base.\n\n`;
  fs.writeFileSync(biblePath, header + out.text);
  console.log(`[series-brief] wrote ${biblePath} (model=${out.model_used})`);
  console.log(`[series-brief] next: run yt-series-bootstrap.mjs ${slug} to sync to knowledge base`);
}

main().catch((e) => { console.error(e); process.exit(1); });
