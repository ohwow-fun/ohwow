/**
 * knowledge-ingest.mjs — given a posts-of-interest JSONL (from
 * feed-explore.mjs), synthesize a workspace-specific knowledge brief
 * and upload it to the active ohwow workspace's knowledge store.
 *
 *   IN=/tmp/x-feed.jsonl npx tsx scripts/x-experiments/knowledge-ingest.mjs
 *
 * Flow:
 *   1. Load posts (kept only those with score >= MIN_INGEST).
 *   2. Ask ohwow's LLM (workspace-routed) to produce a brief:
 *      - "What's new in our space right now" (3–8 bullets)
 *      - "Competitors/peer moves worth tracking"
 *      - "Ideas to steal or react to"
 *      Each bullet cites the permalink(s) that back it.
 *   3. Wrap brief + raw posts into a dated markdown document.
 *   4. Upload via _ohwow.ingestKnowledgeFile → agents can now search it.
 *
 * Workspace-agnostic: picks up whatever workspace the current ohwow
 * session is bound to (OHWOW_WORKSPACE env or ~/.ohwow/current-workspace).
 * The TOPIC env var is what differentiates "what is state of the art for
 * THIS workspace" — defaults to ohwow's positioning, but drop-in swap
 * for any other customer.
 */
import fs from 'node:fs';
import { llm, ingestKnowledgeFile, resolveOhwow, extractJson } from './_ohwow.mjs';
import { propose } from './_approvals.mjs';

const IN = process.env.IN || '/tmp/x-feed.jsonl';
const MIN_INGEST = Number(process.env.MIN_INGEST || 0.35);
const MAX_POSTS = Number(process.env.MAX_POSTS || 40);
const TOPIC = process.env.TOPIC || 'local-first AI agent runtimes, MCP, multi-workspace orchestration, browser automation, dev tools';
const WORKSPACE_PITCH = process.env.WORKSPACE_PITCH || 'ohwow: local-first AI runtime running multiple agent workspaces on one machine, MCP-first, Ollama + Anthropic routing, browser-control and desktop-control built in.';
const DRY = process.env.DRY === '1';

if (!fs.existsSync(IN)) { console.error(`IN not found: ${IN}`); process.exit(1); }

const posts = fs.readFileSync(IN, 'utf8')
  .split(/\n+/).filter(Boolean).map(l => JSON.parse(l))
  .filter(p => (p.score ?? 0) >= MIN_INGEST)
  .sort((a, b) => (b.score ?? 0) - (a.score ?? 0))
  .slice(0, MAX_POSTS);

console.log(`[ingest] ${posts.length} posts after filter (score>=${MIN_INGEST}, cap=${MAX_POSTS})`);

const { workspace } = resolveOhwow();
console.log(`[ingest] active workspace: ${workspace}`);

const synthesisSystem = `You produce a tight intelligence brief for a specific product team.
The team: ${WORKSPACE_PITCH}
Their current watch-area: ${TOPIC}

Given raw tweets gathered from their audience feed, produce STRICT JSON:
{
  "headline": "<=12 words summarizing the theme this batch surfaced",
  "whats_new": ["bullet citing perma=/author/status/id", ...],   // 3-8 items
  "peer_moves": ["bullet citing permalink", ...],                 // 0-6 items
  "ideas_to_steal_or_react_to": ["bullet citing permalink", ...], // 0-6 items
  "skip_list": ["permalink — one-line why we skip", ...]          // things not worth the team's attention
}
Each bullet must end with "(perma=<permalink>)". Concrete over generic. No corporate speak.`;

const postsBlob = posts.map(p =>
  `perma=${p.permalink} @${p.author} · ${p.likes}♥ ${p.replies}💬 · ${p.text.slice(0, 300).replace(/\n/g, ' ')}`,
).join('\n');

const resp = await llm({ purpose: 'reasoning', system: synthesisSystem, prompt: postsBlob });
console.log(`[ingest] synthesized via ${resp.model_used} (${resp.tokens?.input}→${resp.tokens?.output} tok)`);

let brief;
try { brief = extractJson(resp.text); }
catch (e) { console.error('[ingest] JSON parse failed, saving raw:', e.message); brief = { headline: 'parse-failed', raw: resp.text }; }

const today = new Date().toISOString().slice(0, 10);
const md = [
  `# X Feed Intelligence Brief — ${today}`,
  ``,
  `_Workspace: \`${workspace}\` · Topic: ${TOPIC}_`,
  `_Generated by knowledge-ingest.mjs over ${posts.length} posts (score>=${MIN_INGEST})_`,
  ``,
  brief.headline ? `**Headline:** ${brief.headline}\n` : '',
  brief.whats_new?.length ? `## What's new\n${brief.whats_new.map(b => `- ${b}`).join('\n')}\n` : '',
  brief.peer_moves?.length ? `## Peer moves\n${brief.peer_moves.map(b => `- ${b}`).join('\n')}\n` : '',
  brief.ideas_to_steal_or_react_to?.length ? `## Ideas to steal or react to\n${brief.ideas_to_steal_or_react_to.map(b => `- ${b}`).join('\n')}\n` : '',
  brief.skip_list?.length ? `## Skipped\n${brief.skip_list.map(b => `- ${b}`).join('\n')}\n` : '',
  `---`,
  ``,
  `## Raw posts (top ${posts.length})`,
  ``,
  ...posts.map(p => `- **@${p.author}** (${p.likes}♥ ${p.replies}💬 · score=${p.score?.toFixed(2)}): ${p.text.slice(0, 280).replace(/\n/g, ' ')} ([link](https://x.com${p.permalink}))`),
].filter(Boolean).join('\n');

const draftPath = `/tmp/x-brief-${today}.md`;
fs.writeFileSync(draftPath, md);
console.log(`[ingest] wrote draft to ${draftPath} (${md.length} chars)`);

if (DRY) { console.log('[ingest] DRY=1 — not uploading'); process.exit(0); }

const entry = propose({
  kind: 'knowledge_upload',
  summary: `X brief ${today}: ${brief.headline || '(no headline)'} · ${posts.length} posts`,
  payload: { title: `X intelligence brief — ${today}`, filename: `x-brief-${today}.md`, body: md },
  autoApproveAfter: 3, // knowledge is low-risk — trust earlier
});
console.log(`[ingest] approval ${entry.status} · id=${entry.id.slice(0,8)} · trust: ${JSON.stringify(entry.trustStats)}`);
if (entry.status === 'auto_applied') {
  const ing = await ingestKnowledgeFile({ title: entry.payload.title, filename: entry.payload.filename, body: entry.payload.body });
  console.log(`[ingest] auto-applied → ${ing.uploaded?.[0]?.id}`);
} else {
  console.log(`[ingest] pending review — approval-queue.mjs approve ${entry.id.slice(0,8)}`);
}
